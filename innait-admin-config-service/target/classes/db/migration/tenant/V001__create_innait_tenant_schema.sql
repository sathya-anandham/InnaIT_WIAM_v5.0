--------------------------------------------------------
-- TENANTS
--------------------------------------------------------
CREATE TABLE TENANTS (
    TENANT_ID            RAW(16)           DEFAULT SYS_GUID() NOT NULL,
    TENANT_CODE          VARCHAR2(50)      NOT NULL,
    TENANT_NAME          VARCHAR2(255)     NOT NULL,
    STATUS               VARCHAR2(20)      DEFAULT 'ACTIVE' NOT NULL,
    BRANDING_CONFIG      CLOB              DEFAULT '{}',
    SUBSCRIPTION_TIER    VARCHAR2(30)      DEFAULT 'STANDARD' NOT NULL,
    CREATED_AT           TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_AT           TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    VERSION              NUMBER(19)        DEFAULT 0 NOT NULL,
    CONSTRAINT PK_TENANTS PRIMARY KEY (TENANT_ID),
    CONSTRAINT UQ_TENANTS_CODE UNIQUE (TENANT_CODE),
    CONSTRAINT CHK_TENANTS_STATUS CHECK (STATUS IN ('ACTIVE','INACTIVE','SUSPENDED','PENDING_SETUP')),
    CONSTRAINT CHK_TENANTS_TIER CHECK (SUBSCRIPTION_TIER IN ('FREE','STANDARD','PREMIUM','ENTERPRISE')),
    CONSTRAINT CHK_TENANTS_BRANDING CHECK (BRANDING_CONFIG IS JSON)
);

CREATE INDEX IDX_TENANTS_STATUS ON TENANTS (STATUS);
CREATE INDEX IDX_TENANTS_TIER ON TENANTS (SUBSCRIPTION_TIER);

--------------------------------------------------------
-- TENANT_DOMAINS
--------------------------------------------------------
CREATE TABLE TENANT_DOMAINS (
    DOMAIN_ID            RAW(16)           DEFAULT SYS_GUID() NOT NULL,
    TENANT_ID            RAW(16)           NOT NULL,
    DOMAIN_NAME          VARCHAR2(255)     NOT NULL,
    VERIFICATION_STATUS  VARCHAR2(20)      DEFAULT 'PENDING' NOT NULL,
    VERIFICATION_TOKEN   VARCHAR2(255),
    VERIFIED_AT          TIMESTAMP WITH TIME ZONE,
    IS_PRIMARY           NUMBER(1)         DEFAULT 0 NOT NULL,
    CREATED_AT           TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_AT           TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT PK_TENANT_DOMAINS PRIMARY KEY (DOMAIN_ID),
    CONSTRAINT FK_TD_TENANT FOREIGN KEY (TENANT_ID) REFERENCES TENANTS (TENANT_ID),
    CONSTRAINT UQ_TD_DOMAIN UNIQUE (LOWER(DOMAIN_NAME)),
    CONSTRAINT CHK_TD_STATUS CHECK (VERIFICATION_STATUS IN ('PENDING','VERIFIED','FAILED','EXPIRED')),
    CONSTRAINT CHK_TD_PRIMARY CHECK (IS_PRIMARY IN (0, 1))
);

CREATE INDEX IDX_TD_TENANT ON TENANT_DOMAINS (TENANT_ID);
CREATE INDEX IDX_TD_DOMAIN_LOWER ON TENANT_DOMAINS (LOWER(DOMAIN_NAME));

--------------------------------------------------------
-- ORG_UNITS
--------------------------------------------------------
CREATE TABLE ORG_UNITS (
    ORG_UNIT_ID          RAW(16)           DEFAULT SYS_GUID() NOT NULL,
    TENANT_ID            RAW(16)           NOT NULL,
    ORG_CODE             VARCHAR2(100)     NOT NULL,
    ORG_NAME             VARCHAR2(255)     NOT NULL,
    PARENT_ORG_UNIT_ID   RAW(16),
    DESCRIPTION          VARCHAR2(1000),
    STATUS               VARCHAR2(20)      DEFAULT 'ACTIVE' NOT NULL,
    CREATED_AT           TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_AT           TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    VERSION              NUMBER(19)        DEFAULT 0 NOT NULL,
    CONSTRAINT PK_ORG_UNITS PRIMARY KEY (ORG_UNIT_ID),
    CONSTRAINT FK_OU_TENANT FOREIGN KEY (TENANT_ID) REFERENCES TENANTS (TENANT_ID),
    CONSTRAINT FK_OU_PARENT FOREIGN KEY (PARENT_ORG_UNIT_ID) REFERENCES ORG_UNITS (ORG_UNIT_ID),
    CONSTRAINT UQ_OU_TENANT_CODE UNIQUE (TENANT_ID, ORG_CODE),
    CONSTRAINT CHK_OU_STATUS CHECK (STATUS IN ('ACTIVE','INACTIVE'))
);

CREATE INDEX IDX_OU_TENANT ON ORG_UNITS (TENANT_ID);
CREATE INDEX IDX_OU_PARENT ON ORG_UNITS (PARENT_ORG_UNIT_ID);

--------------------------------------------------------
-- APPLICATIONS
--------------------------------------------------------
CREATE TABLE APPLICATIONS (
    APP_ID               RAW(16)           DEFAULT SYS_GUID() NOT NULL,
    TENANT_ID            RAW(16)           NOT NULL,
    APP_CODE             VARCHAR2(100)     NOT NULL,
    APP_NAME             VARCHAR2(255)     NOT NULL,
    APP_TYPE             VARCHAR2(30)      NOT NULL,
    STATUS               VARCHAR2(20)      DEFAULT 'ACTIVE' NOT NULL,
    APP_URL              VARCHAR2(2000),
    DESCRIPTION          VARCHAR2(1000),
    CREATED_AT           TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_AT           TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    VERSION              NUMBER(19)        DEFAULT 0 NOT NULL,
    CONSTRAINT PK_APPLICATIONS PRIMARY KEY (APP_ID),
    CONSTRAINT FK_APP_TENANT FOREIGN KEY (TENANT_ID) REFERENCES TENANTS (TENANT_ID),
    CONSTRAINT UQ_APP_TENANT_CODE UNIQUE (TENANT_ID, APP_CODE),
    CONSTRAINT CHK_APP_TYPE CHECK (APP_TYPE IN ('WEB','SPA','NATIVE','API','SAML_SP','OIDC_RP')),
    CONSTRAINT CHK_APP_STATUS CHECK (STATUS IN ('ACTIVE','INACTIVE','DEPRECATED'))
);

CREATE INDEX IDX_APP_TENANT ON APPLICATIONS (TENANT_ID);
CREATE INDEX IDX_APP_STATUS ON APPLICATIONS (TENANT_ID, STATUS);
