-- ============================================================================
-- InnaIT WIAM 5.0 - INNAIT_SESSION Schema
-- V001: Sessions, session context, refresh tokens, session events
-- ============================================================================

--------------------------------------------------------
-- SESSIONS
--------------------------------------------------------
CREATE TABLE SESSIONS (
    SESSION_ID           RAW(16)           DEFAULT SYS_GUID() NOT NULL,
    TENANT_ID            RAW(16)           NOT NULL,
    ACCOUNT_ID           RAW(16)           NOT NULL,
    SESSION_TYPE         VARCHAR2(20)      DEFAULT 'INTERACTIVE' NOT NULL,
    SESSION_STATUS       VARCHAR2(20)      DEFAULT 'ACTIVE' NOT NULL,
    AUTH_TXN_ID          RAW(16),
    AUTH_LEVEL           NUMBER(2)         DEFAULT 1 NOT NULL,
    STARTED_AT           TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    LAST_ACTIVITY_AT     TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    EXPIRES_AT           TIMESTAMP WITH TIME ZONE NOT NULL,
    TERMINATED_AT        TIMESTAMP WITH TIME ZONE,
    TERMINATION_REASON   VARCHAR2(50),
    CREATED_AT           TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_AT           TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    VERSION              NUMBER(19)        DEFAULT 0 NOT NULL,
    CONSTRAINT PK_SESSIONS PRIMARY KEY (SESSION_ID),
    CONSTRAINT CHK_SESS_TYPE CHECK (SESSION_TYPE IN ('INTERACTIVE','API','SSO','IMPERSONATION')),
    CONSTRAINT CHK_SESS_STATUS CHECK (SESSION_STATUS IN ('ACTIVE','IDLE','EXPIRED','TERMINATED','REVOKED'))
);

CREATE INDEX IDX_SESS_TENANT ON SESSIONS (TENANT_ID);
CREATE INDEX IDX_SESS_ACCOUNT ON SESSIONS (ACCOUNT_ID);
CREATE INDEX IDX_SESS_STATUS ON SESSIONS (TENANT_ID, SESSION_STATUS);
CREATE INDEX IDX_SESS_EXPIRES ON SESSIONS (EXPIRES_AT);

--------------------------------------------------------
-- SESSION_CONTEXT
--------------------------------------------------------
CREATE TABLE SESSION_CONTEXT (
    SESSION_CONTEXT_ID   RAW(16)           DEFAULT SYS_GUID() NOT NULL,
    SESSION_ID           RAW(16)           NOT NULL,
    TENANT_ID            RAW(16)           NOT NULL,
    IP_ADDRESS           VARCHAR2(45)      NOT NULL,
    USER_AGENT           VARCHAR2(1000),
    DEVICE_FINGERPRINT   VARCHAR2(512),
    GEO_COUNTRY          VARCHAR2(3),
    GEO_REGION           VARCHAR2(100),
    GEO_CITY             VARCHAR2(100),
    GEO_LATITUDE         NUMBER(10,7),
    GEO_LONGITUDE        NUMBER(10,7),
    DEVICE_TRUST_SCORE   NUMBER(5,2),
    CREATED_AT           TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT PK_SESS_CTX PRIMARY KEY (SESSION_CONTEXT_ID),
    CONSTRAINT FK_SC_SESSION FOREIGN KEY (SESSION_ID) REFERENCES SESSIONS (SESSION_ID)
);

CREATE INDEX IDX_SC_SESSION ON SESSION_CONTEXT (SESSION_ID);
CREATE INDEX IDX_SC_TENANT ON SESSION_CONTEXT (TENANT_ID);
CREATE INDEX IDX_SC_IP ON SESSION_CONTEXT (IP_ADDRESS);

--------------------------------------------------------
-- REFRESH_TOKENS
--------------------------------------------------------
CREATE TABLE REFRESH_TOKENS (
    REFRESH_TOKEN_ID     RAW(16)           DEFAULT SYS_GUID() NOT NULL,
    SESSION_ID           RAW(16)           NOT NULL,
    TENANT_ID            RAW(16)           NOT NULL,
    TOKEN_HASH           VARCHAR2(512)     NOT NULL,
    TOKEN_FAMILY         RAW(16)           NOT NULL,
    IS_USED              NUMBER(1)         DEFAULT 0 NOT NULL,
    ISSUED_AT            TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    EXPIRES_AT           TIMESTAMP WITH TIME ZONE NOT NULL,
    USED_AT              TIMESTAMP WITH TIME ZONE,
    REVOKED_AT           TIMESTAMP WITH TIME ZONE,
    CREATED_AT           TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT PK_REFRESH_TOKEN PRIMARY KEY (REFRESH_TOKEN_ID),
    CONSTRAINT FK_RT_SESSION FOREIGN KEY (SESSION_ID) REFERENCES SESSIONS (SESSION_ID),
    CONSTRAINT UQ_RT_HASH UNIQUE (TOKEN_HASH),
    CONSTRAINT CHK_RT_USED CHECK (IS_USED IN (0, 1))
);

CREATE INDEX IDX_RT_SESSION ON REFRESH_TOKENS (SESSION_ID);
CREATE INDEX IDX_RT_TENANT ON REFRESH_TOKENS (TENANT_ID);
CREATE INDEX IDX_RT_FAMILY ON REFRESH_TOKENS (TOKEN_FAMILY);
CREATE INDEX IDX_RT_HASH ON REFRESH_TOKENS (TOKEN_HASH);

--------------------------------------------------------
-- SESSION_EVENTS (interval-partitioned by EVENT_TIME, monthly)
--------------------------------------------------------
CREATE TABLE SESSION_EVENTS (
    SESSION_EVENT_ID     RAW(16)           DEFAULT SYS_GUID() NOT NULL,
    SESSION_ID           RAW(16)           NOT NULL,
    TENANT_ID            RAW(16)           NOT NULL,
    EVENT_TYPE           VARCHAR2(50)      NOT NULL,
    EVENT_DATA           CLOB,
    EVENT_TIME           TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT PK_SESS_EVENT PRIMARY KEY (SESSION_EVENT_ID, EVENT_TIME),
    CONSTRAINT CHK_SE_DATA CHECK (EVENT_DATA IS JSON OR EVENT_DATA IS NULL)
)
PARTITION BY RANGE (EVENT_TIME) INTERVAL (NUMTOYMINTERVAL(1, 'MONTH'))
(
    PARTITION P_INIT VALUES LESS THAN (TIMESTAMP '2025-01-01 00:00:00 +00:00')
);

CREATE INDEX IDX_SE_SESSION ON SESSION_EVENTS (SESSION_ID) LOCAL;
CREATE INDEX IDX_SE_TENANT ON SESSION_EVENTS (TENANT_ID) LOCAL;
CREATE INDEX IDX_SE_TYPE ON SESSION_EVENTS (EVENT_TYPE) LOCAL;
