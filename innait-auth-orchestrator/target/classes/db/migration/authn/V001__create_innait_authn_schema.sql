--------------------------------------------------------
-- AUTH_TRANSACTIONS (interval-partitioned by STARTED_AT, monthly)
--------------------------------------------------------
CREATE TABLE AUTH_TRANSACTIONS (
    AUTH_TXN_ID          RAW(16)           DEFAULT SYS_GUID() NOT NULL,
    TENANT_ID            RAW(16)           NOT NULL,
    ACCOUNT_ID           RAW(16),
    CORRELATION_ID       RAW(16)           NOT NULL,
    CURRENT_STATE        VARCHAR2(30)      DEFAULT 'INITIATED' NOT NULL,
    CHANNEL_TYPE         VARCHAR2(20)      DEFAULT 'WEB' NOT NULL,
    CLIENT_IP            VARCHAR2(45),
    USER_AGENT           VARCHAR2(1000),
    RISK_SCORE           NUMBER(5,2),
    DEVICE_CONTEXT_ID    RAW(16),
    STARTED_AT           TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    COMPLETED_AT         TIMESTAMP WITH TIME ZONE,
    EXPIRES_AT           TIMESTAMP WITH TIME ZONE NOT NULL,
    CREATED_AT           TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_AT           TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT PK_AUTH_TXN PRIMARY KEY (AUTH_TXN_ID, STARTED_AT),
    CONSTRAINT CHK_ATXN_STATE CHECK (CURRENT_STATE IN ('INITIATED','IDENTIFIER_COLLECTED','PRIMARY_CHALLENGE','PRIMARY_VERIFIED','MFA_REQUIRED','MFA_CHALLENGE','COMPLETED','FAILED')),
    CONSTRAINT CHK_ATXN_CHANNEL CHECK (CHANNEL_TYPE IN ('WEB','MOBILE','API','KIOSK'))
)
PARTITION BY RANGE (STARTED_AT) INTERVAL (NUMTOYMINTERVAL(1, 'MONTH'))
(
    PARTITION P_INIT VALUES LESS THAN (TIMESTAMP '2025-01-01 00:00:00 +00:00')
);

CREATE INDEX IDX_ATXN_TENANT ON AUTH_TRANSACTIONS (TENANT_ID) LOCAL;
CREATE INDEX IDX_ATXN_ACCOUNT ON AUTH_TRANSACTIONS (ACCOUNT_ID) LOCAL;
CREATE INDEX IDX_ATXN_CORR ON AUTH_TRANSACTIONS (CORRELATION_ID) LOCAL;
CREATE INDEX IDX_ATXN_STATE ON AUTH_TRANSACTIONS (TENANT_ID, CURRENT_STATE) LOCAL;

--------------------------------------------------------
-- AUTH_CHALLENGES
--------------------------------------------------------
CREATE TABLE AUTH_CHALLENGES (
    CHALLENGE_ID         RAW(16)           DEFAULT SYS_GUID() NOT NULL,
    AUTH_TXN_ID          RAW(16)           NOT NULL,
    TENANT_ID            RAW(16)           NOT NULL,
    CHALLENGE_TYPE       VARCHAR2(30)      NOT NULL,
    CHALLENGE_STATUS     VARCHAR2(20)      DEFAULT 'PENDING' NOT NULL,
    CHALLENGE_DATA       CLOB,
    ATTEMPTS             NUMBER(3)         DEFAULT 0 NOT NULL,
    MAX_ATTEMPTS         NUMBER(3)         DEFAULT 3 NOT NULL,
    ISSUED_AT            TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    EXPIRES_AT           TIMESTAMP WITH TIME ZONE NOT NULL,
    VERIFIED_AT          TIMESTAMP WITH TIME ZONE,
    CREATED_AT           TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_AT           TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT PK_AUTH_CHALLENGE PRIMARY KEY (CHALLENGE_ID),
    CONSTRAINT CHK_AC_TYPE CHECK (CHALLENGE_TYPE IN ('PASSWORD','FIDO','TOTP','OTP_EMAIL','OTP_SMS','PUSH','MAGIC_LINK')),
    CONSTRAINT CHK_AC_STATUS CHECK (CHALLENGE_STATUS IN ('PENDING','VERIFIED','FAILED','EXPIRED','CANCELLED'))
);

CREATE INDEX IDX_AC_TXN ON AUTH_CHALLENGES (AUTH_TXN_ID);
CREATE INDEX IDX_AC_TENANT ON AUTH_CHALLENGES (TENANT_ID);
CREATE INDEX IDX_AC_TYPE ON AUTH_CHALLENGES (CHALLENGE_TYPE, CHALLENGE_STATUS);

--------------------------------------------------------
-- AUTH_RESULTS
--------------------------------------------------------
CREATE TABLE AUTH_RESULTS (
    AUTH_RESULT_ID       RAW(16)           DEFAULT SYS_GUID() NOT NULL,
    AUTH_TXN_ID          RAW(16)           NOT NULL,
    TENANT_ID            RAW(16)           NOT NULL,
    RESULT               VARCHAR2(20)      NOT NULL,
    AUTH_METHODS_USED    CLOB              DEFAULT '[]',
    FAILURE_REASON       VARCHAR2(100),
    SESSION_ID           RAW(16),
    CREATED_AT           TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT PK_AUTH_RESULT PRIMARY KEY (AUTH_RESULT_ID),
    CONSTRAINT CHK_ARES_RESULT CHECK (RESULT IN ('SUCCESS','FAILURE','TIMEOUT','CANCELLED')),
    CONSTRAINT CHK_ARES_METHODS CHECK (AUTH_METHODS_USED IS JSON)
);

CREATE INDEX IDX_ARES_TXN ON AUTH_RESULTS (AUTH_TXN_ID);
CREATE INDEX IDX_ARES_TENANT ON AUTH_RESULTS (TENANT_ID);

--------------------------------------------------------
-- LOGIN_ATTEMPTS (hash-partitioned by TENANT_ID, 8 partitions)
--------------------------------------------------------
CREATE TABLE LOGIN_ATTEMPTS (
    ATTEMPT_ID           RAW(16)           DEFAULT SYS_GUID() NOT NULL,
    TENANT_ID            RAW(16)           NOT NULL,
    ACCOUNT_ID           RAW(16),
    LOGIN_ID             VARCHAR2(320),
    CLIENT_IP            VARCHAR2(45)      NOT NULL,
    USER_AGENT           VARCHAR2(1000),
    ATTEMPT_STATUS       VARCHAR2(20)      NOT NULL,
    FAILURE_REASON       VARCHAR2(100),
    ATTEMPTED_AT         TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT PK_LOGIN_ATTEMPT PRIMARY KEY (ATTEMPT_ID, TENANT_ID),
    CONSTRAINT CHK_LA_STATUS CHECK (ATTEMPT_STATUS IN ('SUCCESS','FAILURE','LOCKED','BLOCKED'))
)
PARTITION BY HASH (TENANT_ID) PARTITIONS 8;

CREATE INDEX IDX_LA_TENANT ON LOGIN_ATTEMPTS (TENANT_ID) LOCAL;
CREATE INDEX IDX_LA_ACCOUNT ON LOGIN_ATTEMPTS (ACCOUNT_ID) LOCAL;
CREATE INDEX IDX_LA_IP ON LOGIN_ATTEMPTS (CLIENT_IP) LOCAL;
CREATE INDEX IDX_LA_TIME ON LOGIN_ATTEMPTS (TENANT_ID, ATTEMPTED_AT DESC) LOCAL;
