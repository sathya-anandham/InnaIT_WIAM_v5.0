-- System Roles (MERGE to make idempotent)
-- Uses a fixed TENANT_ID of all-zeros for system-level roles
MERGE INTO ROLES tgt
USING (
    SELECT HEXTORAW('00000000000000000000000000000000') AS TENANT_ID, 'SUPER_ADMIN' AS ROLE_CODE, 'Super Administrator' AS ROLE_NAME, 'ADMIN' AS ROLE_TYPE FROM DUAL UNION ALL
    SELECT HEXTORAW('00000000000000000000000000000000'), 'TENANT_ADMIN', 'Tenant Administrator', 'ADMIN' FROM DUAL UNION ALL
    SELECT HEXTORAW('00000000000000000000000000000000'), 'USER_ADMIN', 'User Administrator', 'ADMIN' FROM DUAL UNION ALL
    SELECT HEXTORAW('00000000000000000000000000000000'), 'HELPDESK', 'Helpdesk Operator', 'ADMIN' FROM DUAL UNION ALL
    SELECT HEXTORAW('00000000000000000000000000000000'), 'AUDITOR', 'Auditor', 'ADMIN' FROM DUAL UNION ALL
    SELECT HEXTORAW('00000000000000000000000000000000'), 'POLICY_ADMIN', 'Policy Administrator', 'ADMIN' FROM DUAL UNION ALL
    SELECT HEXTORAW('00000000000000000000000000000000'), 'IGA_ADMIN', 'IGA Administrator', 'ADMIN' FROM DUAL UNION ALL
    SELECT HEXTORAW('00000000000000000000000000000000'), 'IGA_CERTIFIER', 'IGA Certifier', 'BUSINESS' FROM DUAL UNION ALL
    SELECT HEXTORAW('00000000000000000000000000000000'), 'AUTHENTICATED_USER', 'Authenticated User', 'RUNTIME' FROM DUAL
) src ON (tgt.TENANT_ID = src.TENANT_ID AND tgt.ROLE_CODE = src.ROLE_CODE)
WHEN NOT MATCHED THEN INSERT (ROLE_ID, TENANT_ID, ROLE_CODE, ROLE_NAME, ROLE_TYPE, IS_SYSTEM, STATUS, CREATED_AT, UPDATED_AT)
VALUES (SYS_GUID(), src.TENANT_ID, src.ROLE_CODE, src.ROLE_NAME, src.ROLE_TYPE, 1, 'ACTIVE', SYSTIMESTAMP, SYSTIMESTAMP);
