-- ACCOUNT_ROLE_MAP
--------------------------------------------------------
CREATE TABLE ACCOUNT_ROLE_MAP (
    ACCOUNT_ROLE_ID      RAW(16)           DEFAULT SYS_GUID() NOT NULL,
    ACCOUNT_ID           RAW(16)           NOT NULL,
    ROLE_ID              RAW(16)           NOT NULL,
    TENANT_ID            RAW(16)           NOT NULL,
    ASSIGNMENT_SOURCE    VARCHAR2(30)      DEFAULT 'MANUAL' NOT NULL,
    IS_ACTIVE            NUMBER(1)         DEFAULT 1 NOT NULL,
    ASSIGNED_AT          TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    REMOVED_AT           TIMESTAMP WITH TIME ZONE,
    CREATED_AT           TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_AT           TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT PK_ACCT_ROLE PRIMARY KEY (ACCOUNT_ROLE_ID),
    CONSTRAINT FK_AR_ACCOUNT FOREIGN KEY (ACCOUNT_ID) REFERENCES ACCOUNTS (ACCOUNT_ID),
    CONSTRAINT FK_AR_ROLE FOREIGN KEY (ROLE_ID) REFERENCES ROLES (ROLE_ID),
    CONSTRAINT UQ_AR_ACCT_ROLE UNIQUE (ACCOUNT_ID, ROLE_ID, ASSIGNMENT_SOURCE),
    CONSTRAINT CHK_AR_SOURCE CHECK (ASSIGNMENT_SOURCE IN ('MANUAL','SYNC','POLICY','IGA','SELF_SERVICE')),
    CONSTRAINT CHK_AR_ACTIVE CHECK (IS_ACTIVE IN (0, 1))
);

CREATE INDEX IDX_AR_ACCOUNT ON ACCOUNT_ROLE_MAP (ACCOUNT_ID);
CREATE INDEX IDX_AR_ROLE ON ACCOUNT_ROLE_MAP (ROLE_ID);
CREATE INDEX IDX_AR_TENANT ON ACCOUNT_ROLE_MAP (TENANT_ID);

