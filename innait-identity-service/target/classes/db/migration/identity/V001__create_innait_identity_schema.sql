--------------------------------------------------------
-- USERS
--------------------------------------------------------
CREATE TABLE USERS (
    USER_ID              RAW(16)           DEFAULT SYS_GUID() NOT NULL,
    TENANT_ID            RAW(16)           NOT NULL,
    EMAIL                VARCHAR2(320)     NOT NULL,
    FIRST_NAME           VARCHAR2(100),
    LAST_NAME            VARCHAR2(100),
    DISPLAY_NAME         VARCHAR2(255),
    USER_TYPE            VARCHAR2(30)      DEFAULT 'EMPLOYEE' NOT NULL,
    STATUS               VARCHAR2(30)      DEFAULT 'PENDING_ACTIVATION' NOT NULL,
    IS_DELETED           NUMBER(1)         DEFAULT 0 NOT NULL,
    DELETED_AT           TIMESTAMP WITH TIME ZONE,
    MANAGER_USER_ID      RAW(16),
    ORG_UNIT_ID          RAW(16),
    LOCALE               VARCHAR2(10)      DEFAULT 'en',
    TIMEZONE             VARCHAR2(50)      DEFAULT 'UTC',
    CREATED_AT           TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_AT           TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    VERSION              NUMBER(19)        DEFAULT 0 NOT NULL,
    CONSTRAINT PK_USERS PRIMARY KEY (USER_ID),
    CONSTRAINT FK_USR_MANAGER FOREIGN KEY (MANAGER_USER_ID) REFERENCES USERS (USER_ID),
    CONSTRAINT UQ_USR_TENANT_EMAIL UNIQUE (TENANT_ID, LOWER(EMAIL)),
    CONSTRAINT CHK_USR_TYPE CHECK (USER_TYPE IN ('EMPLOYEE','CONTRACTOR','PARTNER','SERVICE_ACCOUNT','EXTERNAL')),
    CONSTRAINT CHK_USR_STATUS CHECK (STATUS IN ('ACTIVE','INACTIVE','LOCKED','SUSPENDED','PENDING_ACTIVATION','EXPIRED','DEPROVISIONED')),
    CONSTRAINT CHK_USR_DELETED CHECK (IS_DELETED IN (0, 1))
);

CREATE INDEX IDX_USR_TENANT ON USERS (TENANT_ID);
CREATE INDEX IDX_USR_EMAIL ON USERS (TENANT_ID, LOWER(EMAIL));
CREATE INDEX IDX_USR_STATUS ON USERS (TENANT_ID, STATUS);
CREATE INDEX IDX_USR_TYPE ON USERS (TENANT_ID, USER_TYPE);
CREATE INDEX IDX_USR_MANAGER ON USERS (MANAGER_USER_ID);
CREATE INDEX IDX_USR_DELETED ON USERS (TENANT_ID, IS_DELETED);

--------------------------------------------------------
-- ACCOUNTS
--------------------------------------------------------
CREATE TABLE ACCOUNTS (
    ACCOUNT_ID           RAW(16)           DEFAULT SYS_GUID() NOT NULL,
    USER_ID              RAW(16)           NOT NULL,
    TENANT_ID            RAW(16)           NOT NULL,
    LOGIN_ID             VARCHAR2(320)     NOT NULL,
    ACCOUNT_STATUS       VARCHAR2(30)      DEFAULT 'ACTIVE' NOT NULL,
    IS_PASSWORD_ENABLED  NUMBER(1)         DEFAULT 1 NOT NULL,
    IS_FIDO_ENABLED      NUMBER(1)         DEFAULT 0 NOT NULL,
    IS_TOTP_ENABLED      NUMBER(1)         DEFAULT 0 NOT NULL,
    IS_SOFTTOKEN_ENABLED NUMBER(1)         DEFAULT 0 NOT NULL,
    FAILED_ATTEMPT_COUNT NUMBER(5)         DEFAULT 0 NOT NULL,
    LOCKED_UNTIL         TIMESTAMP WITH TIME ZONE,
    LAST_LOGIN_AT        TIMESTAMP WITH TIME ZONE,
    CREATED_AT           TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_AT           TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    VERSION              NUMBER(19)        DEFAULT 0 NOT NULL,
    CONSTRAINT PK_ACCOUNTS PRIMARY KEY (ACCOUNT_ID),
    CONSTRAINT FK_ACCT_USER FOREIGN KEY (USER_ID) REFERENCES USERS (USER_ID),
    CONSTRAINT UQ_ACCT_TENANT_LOGIN UNIQUE (TENANT_ID, LOWER(LOGIN_ID)),
    CONSTRAINT CHK_ACCT_STATUS CHECK (ACCOUNT_STATUS IN ('ACTIVE','INACTIVE','LOCKED','SUSPENDED','PENDING_ACTIVATION','EXPIRED','DEPROVISIONED')),
    CONSTRAINT CHK_ACCT_PWD CHECK (IS_PASSWORD_ENABLED IN (0, 1)),
    CONSTRAINT CHK_ACCT_FIDO CHECK (IS_FIDO_ENABLED IN (0, 1)),
    CONSTRAINT CHK_ACCT_TOTP CHECK (IS_TOTP_ENABLED IN (0, 1)),
    CONSTRAINT CHK_ACCT_SOFT CHECK (IS_SOFTTOKEN_ENABLED IN (0, 1))
);

CREATE INDEX IDX_ACCT_USER ON ACCOUNTS (USER_ID);
CREATE INDEX IDX_ACCT_TENANT ON ACCOUNTS (TENANT_ID);
CREATE INDEX IDX_ACCT_LOGIN ON ACCOUNTS (TENANT_ID, LOWER(LOGIN_ID));
CREATE INDEX IDX_ACCT_STATUS ON ACCOUNTS (TENANT_ID, ACCOUNT_STATUS);

--------------------------------------------------------
-- ROLES
--------------------------------------------------------
CREATE TABLE ROLES (
    ROLE_ID              RAW(16)           DEFAULT SYS_GUID() NOT NULL,
    TENANT_ID            RAW(16)           NOT NULL,
    ROLE_CODE            VARCHAR2(100)     NOT NULL,
    ROLE_NAME            VARCHAR2(255)     NOT NULL,
    DESCRIPTION          VARCHAR2(1000),
    ROLE_TYPE            VARCHAR2(20)      DEFAULT 'BUSINESS' NOT NULL,
    IS_SYSTEM            NUMBER(1)         DEFAULT 0 NOT NULL,
    STATUS               VARCHAR2(20)      DEFAULT 'ACTIVE' NOT NULL,
    CREATED_AT           TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_AT           TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    VERSION              NUMBER(19)        DEFAULT 0 NOT NULL,
    CONSTRAINT PK_ROLES PRIMARY KEY (ROLE_ID),
    CONSTRAINT UQ_ROLE_TENANT_CODE UNIQUE (TENANT_ID, ROLE_CODE),
    CONSTRAINT CHK_ROLE_TYPE CHECK (ROLE_TYPE IN ('RUNTIME','BUSINESS','APP','ADMIN')),
    CONSTRAINT CHK_ROLE_SYSTEM CHECK (IS_SYSTEM IN (0, 1)),
    CONSTRAINT CHK_ROLE_STATUS CHECK (STATUS IN ('ACTIVE','INACTIVE'))
);

CREATE INDEX IDX_ROLE_TENANT ON ROLES (TENANT_ID);
CREATE INDEX IDX_ROLE_TYPE ON ROLES (TENANT_ID, ROLE_TYPE);

